package club.gsjblog.springboot.freemark.bean;

public class Hobby {
	
	private String hobbyname; //爱好名称
	private int hobbylike; // 爱好程度
	public String getHobbyname() {
		return hobbyname;
	}
	public void setHobbyname(String hobbyname) {
		this.hobbyname = hobbyname;
	}
	public int getHobbylike() {
		return hobbylike;
	}
	public void setHobbylike(int hobbylike) {
		this.hobbylike = hobbylike;
	}
	
	
}
